var str = "Abhishek";
var bool = true;
var undef;
var nul = null;

console.log(`This is a string: ${str} and its type is ${typeof str}`); // This is a string: Abhishek and its type is string

console.log(`This is a boolean value: ${bool} and its type is ${typeof bool}`); // This is a boolean value: true and its type is boolean

console.log(
  `This is a undefined value: ${undef} and its type is ${typeof undef}` // This is a undefined value: undefined and its type is undefined
);

console.log(`This is a null value: ${nul} and its type is ${typeof nul}`); // This is a null value: null and its type is object
